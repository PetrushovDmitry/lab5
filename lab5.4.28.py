
m1 = [[-3,7,5],[4,3,-1],[2,9,3]]
s = [-1,1,0]
n = len(m1)
d = [0,0,0]
x = [0,0,0]
l = [0,0,0]
d[0] = m1[0][1]/(-m1[0][0])
l[0] = (-s[0])/(-m1[0][0])
for i in range(1,n-1):
    d[i] = m1[i][i+1]/(-m1[i][i]-m1[i][i-1]*d[i-1])
    l[i] = (m1[i][i-1]*l[i-1]-s[i])/(-m1[i][i]-m1[i][i-1]*d[i-1])
d[n-1] = 0
l[n-1] = (m1[n-1][n-2]*l[n-2]-s[n-1])/(-m1[n-1][n-1]-m1[n-1][n-2]*d[n-2])
x[n-1] = l[n-1]
for i in range(n-1,0,-1):
    x[i-1] = d[i-1]*x[i]+l[i-1]
print(x)
    
